package treasurer

import (
	"gorm.io/gorm"

	"github.com/defipod/mochi/pkg/model"
)

type pg struct {
	db *gorm.DB
}

func NewPG(db *gorm.DB) Store {
	return &pg{db: db}
}

func (pg *pg) Create(treasurer *model.Treasurer) (*model.Treasurer, error) {
	return treasurer, pg.db.Create(treasurer).Error
}
